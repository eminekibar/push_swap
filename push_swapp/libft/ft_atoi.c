#include "../push_swap.h"

long	ft_atoi(const char *str)
{
	int		i;
	int		sign;
	long	result;
	int		k;

	i = 0;
	k = 0;
	sign = 1;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i++] - '0';
		k++;
	}
	if (k > 10)
		ft_error();
	return (result * sign);
}
